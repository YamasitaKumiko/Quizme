function popBox() {
    let Box = document.getElementById("popBox");
    let popLayer = document.getElementById("popLayer");
    Box.style.display = "block";
    popLayer.style.display = "block";
}

function hide() {
    let Box = document.getElementById("popBox");
    let popLayer = document.getElementById("popLayer");
    Box.style.display = "none";
    popLayer.style.display = "none";
}
